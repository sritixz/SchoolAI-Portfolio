section("18. Interventions")
r = requests.get(f"{BASE}/teacher/interventions", headers=H)
check("GET /teacher/interventions", r)

# ── 19. Schedule ─────────────────────────────────────────────
section("19. Schedule")
r = requests.get(f"{BASE}/teacher/schedule", headers=H)
check("GET /teacher/schedule", r)

# ── 20. Homework Preview ─────────────────────────────────────
section("20. Homework Preview (by ID)")
if hw_id:
    r = requests.get(f"{BASE}/homework/{hw_id}", headers=H)
    hw_detail = check("GET /homework/:id", r, key="title")
    if hw_detail:
        print(f"       Title: {hw_detail.get('title')} | Questions: {len(hw_detail.get('questions',[]))}")
else:
    print(f"  {WARN} Skipped — no hw_id")

# ── Summary ──────────────────────────────────────────────────
passed = sum(1 for _, ok in results if ok)
failed = sum(1 for _, ok in results if not ok)
total  = len(results)

print(f"\n{'═'*60}")
print(f"  RESULTS: {passed}/{total} passed  |  {failed} failed")
print(f"{'═'*60}")

if failed:
    print("\n  Failed checks:")
    for name, ok in results:
        if not ok:
            print(f"    {FAIL} {name}")
    sys.exit(1)
else:
    print("\n  \033[92mAll teacher AI feature endpoints verified ✓\033[0m\n")
